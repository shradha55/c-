#include<iostream>
using namespace std;
int main()
{
    int a=2,b=5;
    cout<<"Tne operators in c++:"<<endl;
    cout<<"The following are the operations:"<<endl;
    cout<<"the value of a + b is:"<<a+b<<endl;
    cout<<"the value of a - b is:"<<a-b<<endl;
    cout<<"the value of a * b is:"<<a*b<<endl;
    cout<<"the value of a / b is:"<<a/b<<endl;
    cout<<"the value of a % b is:"<<a%b<<endl;
    cout<<"the value of a ++ is:"<<a++<<endl;
    cout<<"the value of a -- is:"<<a--<<endl;
    cout<<"the value of ++ a is:"<<++a<<endl;
    cout<<"the value of -- a is:"<<--a<<endl;
    cout<<"the value of b ++ is:"<<b++<<endl;
    cout<<"the value of b -- is:"<<b--<<endl;
    cout<<"the value of ++ b is:"<<++b<<endl;
    cout<<"the value of -- b is:"<<--b<<endl;
    return 0;
}